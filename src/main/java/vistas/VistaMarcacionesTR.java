    HiloMarcacion hilo;
    private void buscar() {
        String nroDocumento;
        if(empleadoSeleccionado == null){
            nroDocumento = null;
        }else{
            nroDocumento = empleadoSeleccionado.getNroDocumento();
        }
        if(hilo == null){
            hilo = new HiloMarcacion(nroDocumento, new Date(), lista);
            hilo.start();
        }else{
            hilo.setDni(nroDocumento);
        }
        
        
        
        
    }
